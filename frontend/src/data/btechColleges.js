// Get all colleges as a flat array for search functionality
export const getAllColleges = () => {
  const allColleges = []
  Object.values(btechColleges).forEach(stateColleges => {
    allColleges.push(...stateColleges)
  })
  return allColleges.sort()
}

// Get colleges by state
export const getCollegesByState = (state) => {
  return btechColleges[state] || []
}

// Get all states
export const getAllStates = () => {
  return Object.keys(btechColleges).sort()
}

// Search colleges by name
export const searchColleges = (searchTerm) => {
  const allColleges = getAllColleges()
  return allColleges.filter(college => 
    college.toLowerCase().includes(searchTerm.toLowerCase())
  )
}